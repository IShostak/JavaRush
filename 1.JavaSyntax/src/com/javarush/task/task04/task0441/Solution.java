import java.io.*;
import java.util.Scanner;

public class Solution {
    public static void main(String[] args) throws Exception {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        int y = sc.nextInt();
        int z = sc.nextInt();
        sc.close();

        System.out.println((x+y+z)-Math.max(x,Math.max(y,z))-Math.min(x,Math.min(y,z)));

    }
}
